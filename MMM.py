def make_double(num_1, num_2):
    print(num_1 * num_2)


def divide(num_1, num_2):
    if num_2 == 0:
        print('на 0 делить нельзя')
    else:
        print(num_1 / num_2)


def fold(num_1, num_2):
    print(num_1 + num_2)


def subtract(num_1, num_2):
    print(num_1 -  num_2)


make_double(4, 2)
divide(4, 2)
fold(4, 2)
subtract(4, 2)
